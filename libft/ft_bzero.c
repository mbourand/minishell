#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*cs;

	cs = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		cs[i] = 0;
		i++;
	}
}
