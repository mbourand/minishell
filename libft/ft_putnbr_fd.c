#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	u;
	char			c;

	u = (n < 0 ? -n : n);
	if (n < 0)
		write(fd, "-", 1);
	if (u >= 10)
		ft_putnbr_fd(u / 10, fd);
	c = u % 10 + '0';
	write(fd, &c, 1);
}
